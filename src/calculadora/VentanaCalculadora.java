    private void visorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_visorKeyTyped
        // TODO add your handling code here:
        char vchar = evt.getKeyChar();
        if (!this.isCharacterAcceptable(vchar)
                || (vchar == KeyEvent.VK_BACK_SPACE) || (vchar == KeyEvent.VK_DELETE)) {
            evt.consume();
        }
        if (((vchar == ',') || (vchar == '.')) && (this.hayMasDeUno(this.visor.getText(), Character.toString(vchar)))) {
            evt.consume();
        }
    }//GEN-LAST:event_visorKeyTyped

